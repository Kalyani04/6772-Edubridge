package opreators;

public class Relational {

	public static void main(String[] args) {
		int a = 50;
		int b = 100;
		
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		
		System.out.println("--a  = "+(--a));
		System.out.println("a--  = "+(a--));
		System.out.println("--b  = "+(--b));
		System.out.println("b--  = "+(b--));

	}

}
