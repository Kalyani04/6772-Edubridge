package opreators;

public class Bitwise {

	public static void main(String[] args) 
	{
		int a = 4;
		int b = 6;

		System.out.println("a&b is " + (a & b));
		System.out.println("a|b is " + (a | b));
		System.out.println("a^b is " + (a ^ b));
		System.out.println("~a is " + ~a);
	}

}
